def addition(num1, num2)
  total = num1 + num2
  return total
end

def subtraction(num1, num2)
  subtract_total = num1 + num2
  puts subtract_total
end

def division(num1, num2)
  divided_total = num1 / num2
  puts divided_total
end

def multiplication(num1, num2)
  multiply_total = num1 * num2
  puts multiply_total
end

def modulo(num1, num2)
  modulo_total = num1 % num2
  puts modulo_total
end

def square_root(num)
  sqrt_total = Math.sqrt(num)
  puts sqrt_total
end